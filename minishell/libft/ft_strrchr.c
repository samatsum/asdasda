#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*str;
	char		ch;

	str = (const char *)s + ft_strlen(s);
	ch = (char)c;
	while (1)
	{
		if (*str == ch)
			return ((char *)str);
		if (str == s)
			return (NULL);
		str--;
	}
}
