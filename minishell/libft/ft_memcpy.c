#include "libft.h"

// if (d > s) : Backward copy to avoid overlap
// if (d <= s) : Forward copy to avoid overlap
void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char		*d;
	const unsigned char	*s;

	d = (unsigned char *)dst;
	s = (const unsigned char *)src;
	if (n == 0 || d == s)
		return (dst);
	if (d > s)
	{
		while (n--)
			*(d + n) = *(s + n);
	}
	else
	{
		while (n--)
			*d++ = *s++;
	}
	return (dst);
}
