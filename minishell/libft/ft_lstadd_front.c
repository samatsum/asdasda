#include "libft.h"

// lst: not protected
// new: protected (A node can be appended to an empty list(NULL))
void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (new == NULL)
		return ;
	new->next = *lst;
	*lst = new;
}
