#include "libft.h"

// *lst: not protected
// del: not protected(contents must be freed.)
void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*next;

	while (*lst)
	{
		next = (*lst)->next;
		ft_lstdelone((*lst), del);
		*lst = next;
	}
}
